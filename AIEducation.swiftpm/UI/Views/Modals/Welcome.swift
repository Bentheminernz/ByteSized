struct WelcomeComponents {
  @ViewBuilder
  static func WelcomePoint(
    title: String,
    description: String,
    imageName: String
  ) -> some View {
    HStack {
      Image(systemName: imageName)
        .resizable()
        .scaledToFit()
        .frame(width: 40, height: 40)
        .foregroundStyle(.green.gradient)

      VStack(alignment: .leading, spacing: 4) {
        Text(title)
          .font(.title3)
          .fontWeight(.bold)

        Text(description)
          .font(.body)
          .foregroundStyle(.secondary)
          .fixedSize(horizontal: false, vertical: true)
          .lineLimit(nil)
      }
      .padding()
    }
  }

  @ViewBuilder
  static func WelcomePageHeader(
    title: String,
    subtitle: String,
    imageName: String,
    imageColor: Color = .accentColor
  ) -> some View {
    VStack(spacing: 16) {
      Image(systemName: imageName)
        .font(.system(size: 72))
        .foregroundStyle(.white)
        .frame(width: 114, height: 114)
        .background(imageColor.gradient)
        .cornerRadius(20)
        .padding(.bottom, 5)

      VStack {
        Text(title)
          .font(.title)
          .fontWeight(.bold)

        Text(subtitle)
          .font(.subheadline)
          .foregroundColor(.secondary)
          .multilineTextAlignment(.center)
          .fixedSize(horizontal: false, vertical: true)
      }
    }
    .padding()
  }
}
